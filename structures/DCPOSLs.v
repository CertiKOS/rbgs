(** * Dcpo-lattices *)

(** A dcpo-semilattice is a set equipped independently with
  both the structure of a dcpo and that of a semilattice.
  Structures of this kind are mentioned for example
  in §6.2 of Abramsky and Jung's domain theory textbook,
  which discusses various forms of powerdomains.

  For our purposes, the specific structures we combine are:
  - pointed dcpos and strict Scott-continuous functions between them,
    representing spaces of potentially non-terminating computations, and
  - completely distributive lattices, representing computation spaces
    with different kinds of nondeterministic choices.

  Note that both structures involve a partial order, least element and
  supremum operations. In fact, every complete lattice is a dcpo, and
  every sup-preserving function is strict Scott-continuous.
  However, a dcpo-semilattice provides two distinct partial orderings
  of its carrier set, which may or may not have anything to do with
  each other. *)

Class DCPOLattice (L : Type) :=
  {
    dcposl_dcpo :> DCPO L;
    dcposl_sl :> CDLattice L;
    (*sup_dsup*)
  }.


(** ** Free dcpo *)

(** Before we construct the free dcpo-semilattice, we start with the
  free dcpo generated by a set, a standard construction which
  essentially consists of the discrete order extended with
  a bottom element. For our purposes, we will call the elements
  of the free dcpo "[outcome]s", which can either be [val]ues
  from the generating set or bottom element meant to denote
  [div]ergence. *)

Variant outcome {A} := val (a : A) | div.
Arguments outcome : clear implicits.

(** The information ordering on [outcome]s allows [div]
  to be replaced by a terminating outcome. *)

Variant outcome_le {A} : relation (outcome A) :=
  | val_le a : outcome_le (val a) (val a)
  | div_le x : outcome_le div x.

Global Instance outcome_le_preo A :
  PreOrder (@outcome_le A).
Proof.
  split.
  - intros [ | ]; constructor.
  - destruct 1; auto. constructor.
Qed.

Global Instance outcome_le_antisym A :
  Antisymmetric (outcome A) eq outcome_le.
Proof.
  intros x y Hxy Hyx.
  destruct Hxy; inversion Hyx; auto.
Qed.

(** Classically speaking, the order defined above is also a dcpo:
  any directed set will contain at most one of the generators,
  in which case it will be the supremum. If there is no generator
  in the set then the supremum must be [div]. However, it is not
  possible to provide a [dsup] operator as defined in the [DCPO]
  library without the use of classical axioms, as we would need
  the generator membership condition to be decidable for a set
  given in [outcome A -> Prop]. Since we only use the construction
  above as a building block, this is not an issue. *)

(** ** Free semilattice *)

(** On the other side of things, the free sup-lattice generated by a
  set is simply its powerset, ordered under inclusion, where suprema
  are given by set unions. We may want to refactor the code to make
  this explicit somewhere, though this construction is a special case
  of the downset completion mechanized in [lattices/Downset.v]. *)

(** ** Free dcpo-semilattice *)

(** Per Abramsky and Jung, a dcpo-semilattice is a dcpo
  which provides the join operation of a semilattice.
  As a dcpo operator, the join must preserve directed sups
  in each argument. In the infinitary case of the sup-lattices
  we are considering, this manifests as the distributivity property

      ⋃i∈I · ⋁j∈Jᵢ · x[i,j]  =  ⋁f∈(∏i·Jᵢ) · ⋃i∈I · x[i,f(i)]

  where ⋃ denotes the join operation associated with the sup-lattice
  and ⋁ is the directed-sup operation (the least element can be seen
  as the empty sup) associated with the dcpo structure.
  Maps between dcpo-semilattices must respect both structures and
  be at the time strict Scott-continuous with regards to the dcpo
  and sup-preserving with regards the semilattice ordering. *)

Module FDSL.

  (** We will need the following choice principle
    to prove complete distributivity. *)

  Lemma choice_dep :
    DependentFunctionalChoice.
  Proof.
    apply non_dep_dep_functional_choice. red.
    auto using choice.
  Qed.

  (** *** Carrier set *)

  (** The free dcpo-semilattice can be defined using sets of outcomes. *)

  Definition omap A :=
    outcome A -> Prop.

  (** *** Lattice structure *)

  (** The associated sup-lattice structure is induced by the set inclusion
    ordering, where suprema are given by set union. *)

  Global Program Instance omap_poset A : Poset (omap A) :=
    {
      ref := (- ==> impl);
    }.
  Next Obligation.
    split.
    - split; typeclasses eauto.
    - intros x y Hxy Hyx.
      apply functional_extensionality. intros u.
      apply propositional_extensionality.
      firstorder.
  Qed.

  Global Program Instance omap_cdlat A : CDLattice (omap A) :=
    {
      lsup I x u := exists i:I, x i u;
      linf I x u := forall j:I, x j u;
    }.
  Next Obligation.
    firstorder.
  Qed.
  Next Obligation.
    firstorder.
  Qed.
  Next Obligation.
    apply functional_extensionality. intros u.
    apply propositional_extensionality. split.
    - firstorder.
    - intros H1.
      apply NNPP. intros H2.
      assert (H3 : forall i, exists j, ~ x i j u).
      {
        intros i.
        apply not_ex_all_not with (n := i) in H2.
        apply not_all_ex_not in H2.
        assumption.
      }
      pose proof (choice_dep I J (fun i j => ~ x i j u)).
      firstorder.
  Qed.

  (** *** DCPO structure *)

  (** By contrast, the directed-complete structure is more subtle.
    The least element is the singleton {div}. As the computation
    progresses, any [div] outcome in the set can be replaced by a new
    set of outcomes. This new set of outcomes can be empty, and could
    itself include [div] if some of the computation's nondeterministic
    are still running. However, once [div] has been dropped from the
    set, the computation is fully terminated and can no longer be
    modified by further execution. *)

  Section DCPO.
    Context (A : Type).

    (** The order described above can be defined as follows. *)

    Record le (x y : omap A) :=
      {
        le_fw a : x (val a) -> y (val a);
        le_bw σ : y σ -> x div \/ x σ;
      }.

    Lemma le_div (x y : omap A) :
      le x y -> y div -> x div.
    Proof.
      firstorder.
    Qed.

    Global Instance le_preo :
      PreOrder le.
    Proof.
      firstorder.
    Qed.

    Global Instance le_antisym :
      Antisymmetric _ eq le.
    Proof.
      intros x y Hxy Hyx.
      apply functional_extensionality. intros σ.
      apply propositional_extensionality.
      destruct σ; firstorder.
    Qed.

    Global Program Instance le_po : PartialOrder le := {}.

    (** Under that ordering, fully terminating computations are
      the maximal elements. Two computations will have an upper bound
      when it is possible to extend them so that they produce the same
      set of outcomes. Sups for sets of computations which are
      compatible in this way can be defined as follows. *)

    Section DSUP.
      Context {I} (x : I -> omap A) `{Dx : !Directed x}.

      Definition dsup : omap A :=
        fun σ =>
          match σ with
            | val a => exists i, x i (val a)
            | div => forall i, x i div
          end.

      Lemma directed_val i j a :
        x i (val a) -> x j div \/ x j (val a).
      Proof.
        intros Hxa.
        destruct (directed i j) as (k & Hik & Hjk); auto; cbn in *.
        clear - Hxa Hik Hjk.
        firstorder.
      Qed.

      Lemma dsup_has_all σ :
        dsup σ -> forall i, x i div \/ x i σ.
      Proof.
        destruct σ; firstorder.
      Qed.

      Global Instance dsup_spec :
        IsSup x dsup.
      Proof.
        split; cbn.
        - intros i. split.
          + firstorder.
          + intros [a | ]; firstorder.
        - intros y Hy. split.
          + firstorder.
          + intros [a | ] Hσ; [ | firstorder].
            destruct (classic (forall i, x i div)); auto. right.
            apply not_all_ex_not in H as [i Hi].
            firstorder.
      Qed.
    End DSUP.

    Global Instance le_dc : DCPO (omap A) :=
      {
        lce := le;
        dsup I x Hx := dsup x;
      }.
  End DCPO.

  (** ** Monad operations *)

  (** TODO: we could have a proper definition of the [ConcreteCategory]
    of dcpo-semilattice and their homomorphisms and prove the full
    adjunction property, but for now we can start with the induced monad. *)

  Variant ret {A} (a : A) : omap A :=
    | ret_intro : ret a (val a).

  Variant bind {A B} (f : A -> omap B) (x : omap A) : omap B :=
    | bind_div : x div -> bind f x div
    | bind_val a σ : x (val a) -> f a σ -> bind f x σ.
  
  Lemma bind_ret_l {A} (x : omap A) :
    bind (@ret A) x = x.
  Proof.
    apply functional_extensionality. intro σ.
    apply propositional_extensionality. split.
    - destruct 1 as [ | a σ Ha [ ]]; auto.
    - intro Hσ.
      destruct σ; econstructor; eauto.
      constructor.
  Qed.

  Lemma bind_ret_r {A B} (f : A -> omap B) (a : A) :
    bind f (ret a) = f a.
  Proof.
    apply functional_extensionality. intro σ.
    apply propositional_extensionality. split.
    - destruct 1 as [Ha | a' σ Ha Hσ]; inversion Ha; clear Ha; subst; auto.
    - intros Hσ.
      apply (bind_val _ _ a); auto.
      apply ret_intro.
  Qed.

  Lemma bind_bind {A B C} (g : B -> omap C) (f : A -> omap B) (x : omap A) :
    bind g (bind f x) = bind (fun a => bind g (f a)) x.
  Proof.
    apply functional_extensionality. intro σ.
    apply propositional_extensionality. split.
    - destruct 1 as [Ha | a σ Ha Hσ];
      inversion Ha; clear Ha; subst;
      eauto using bind_div, bind_val.
    - intros Hσ.
      destruct Hσ as [Ha | a σ Ha Hσ]; eauto using bind_div.
      destruct Hσ as [Hb | b σ Hb Hσ]; eauto using bind_div, bind_val.
  Qed.

End FDSL.
 
